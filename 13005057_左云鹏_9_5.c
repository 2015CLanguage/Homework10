#include<stdio.h>
#define N 10
struct student
{
	char num[6];
	char name[8];
	float score[3];
	float avr;
}stu[N];

int main()
{
	int i,j,maxi;
	float sum,max,average;
	//输入数据
	for(i=0;i<N;i++)
	{
		printf("请输入第%d个学生的成绩:\n",i+1);
		printf("学号：");
		scanf("%s",stu[i].num);
		printf("姓名：");
		scanf("%s",stu[i].name);
		for (j=0;j<3;j++)
		{
			printf("第%d门课成绩：",j+1);
			scanf("%f",&stu[i].score[j]);
		}

	}
	//计算
	average=0;
	max=0;
	maxi=0;
	for(i=0;i<N;i++)
	{
		sum=0;
		for(j=0;j<3;j++)
			sum=sum+stu[i].score[j];
		stu[i].avr=sum/3.0;
		average=average+stu[i].avr;
		if(sum>max)
		{
			max=sum;
			maxi=i;
		}
	}
	average=average/N;
	//输出
	printf("学号： 姓名： 成绩1： 成绩2： 成绩3： 平均分：\n");
	for(i=0;i<N;i++)
	{
		printf("%5s%10s",stu[i].num,stu[i].name);
		for(j=0;j<3;j++)
		{
			printf("%9.2f",stu[i].score[j]);
			printf("%8.2f\n",stu[i].avr);
		}
	}
	printf("总平均分为：%5.2f\n",average);
	printf("第%s学生有最高分，姓名为%s\n",stu[maxi].num,stu[maxi].name);
	printf("其分数为：%6.2f,%6.2f,%6.2f,平均分为:%5.2f\n",stu[maxi].score[0],stu[maxi].score[1],stu[maxi].score[2],stu[maxi].avr);
	return 0;
}
